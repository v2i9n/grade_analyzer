print("أهلاً في محلل الدرجات حق أول ثانوي 🤖")

الرياضيات = int(input("كم جبت بالرياضيات؟ "))
العلوم = int(input("كم جبت بالعلوم؟ "))
الانجليزي = int(input("كم جبت بالإنجليزي؟ "))
العربي = int(input("كم جبت بالعربي؟ "))
الحاسب = int(input("كم جبت بالحاسب؟ "))

المجموع = الرياضيات + العلوم + الانجليزي + العربي + الحاسب
المعدل = المجموع / 5

print("--------------------------------")
print("معدلك الكلي هو:", المعدل)

if المعدل >= 90:
    print("ممتاز جداً! أنت وحش ما شاء الله 🔥")
elif المعدل >= 80:
    print("جيد جداً، شد حيلك شوي وتوصل 90 💪")
else:
    print("تقدر تجيب أعلى، أنا واثق فيك ❤️")

اقل_درجة = min(الرياضيات, العلوم, الانجليزي, العربي, الحاسب)
print("أقل درجة عندك:", اقل_درجة, "شد حيلك فيها الترم الجاي")
